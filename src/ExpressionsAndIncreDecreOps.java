void main() {
    //Increment and Decrement Operators

    int i = 56;
    System.out.println(i++); //Pehle i ko use kro fir usey increment karo (uski value badha do)

    System.out.println(i); // i ki incremented value

    int j = 60;
    int c = ++j; // Pehle j increment hoga fir jake use hoga
    System.out.println(c);


    int k = 25;
    int x = k;
    System.out.println(k--);
}